package nhs

import (
	"encoding/json"
	"testing"
)

func TestParse_ValidPacket(t *testing.T) {
	data := []byte{
		0x02, 0x01, 0x02, 0x01, 0x56, 0xa9, 0xbe, 0x43, 0x84, 0x49,
		0x1c, 0x03, 0x03, 0x6f, 0xfd,
		0x17, 0x16, 0x6f, 0xfd,
		0xfb, 0x2b, 0xac, 0x2b, 0x83, 0x48, 0x60, 0xc8, 0x88, 0x80,
		0x92, 0x34, 0xb5, 0x2f, 0x11, 0x2d, 0x59, 0x93, 0x40, 0x04, 0xa4,
	}
	got := Parse(data)
	var v map[string]json.RawMessage
	if err := json.Unmarshal([]byte(got), &v); err != nil {
		t.Fatalf("result is not valid JSON: %v (%q)", err, got)
	}
	if _, ok := v["nhs"]; !ok {
		t.Errorf("missing nhs key in %q", got)
	}
}
